-- 重複投稿を削除（全く同じ body が連続している場合、新しい方を残す）
DELETE FROM bbs_comments
WHERE id IN (
  SELECT c1.id
  FROM bbs_comments c1
  JOIN bbs_comments c2
    ON c1.post_id = c2.post_id
    AND c1.body = c2.body
    AND c1.author = c2.author
    AND c1.id > c2.id
);

DELETE FROM bbs_posts
WHERE id IN (
  SELECT p1.id
  FROM bbs_posts p1
  JOIN bbs_posts p2
    ON p1.title = p2.title
    AND p1.body = p2.body
    AND p1.id > p2.id
);

